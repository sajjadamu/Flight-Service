package com.jck.travel.flight.model.dto;

public class FlightStops {
	
	private String stopName;
	
	private String stopCity;
	
	private String stopOverTime;
	
	private String airPortName;
	
	public String getStopName() {
		return stopName;
	}
	public void setStopName(String stopName) {
		this.stopName = stopName;
	}
	public String getStopCity() {
		return stopCity;
	}
	public void setStopCity(String stopCity) {
		this.stopCity = stopCity;
	}
	public String getStopOverTime() {
		return stopOverTime;
	}
	public void setStopOverTime(String stopOverTime) {
		this.stopOverTime = stopOverTime;
	}
	public String getAirPortName() {
		return airPortName;
	}
	public void setAirPortName(String airPortName) {
		this.airPortName = airPortName;
	}
	

}
